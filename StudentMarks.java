package Delinmani;
import java.util.*;
public class StudentMarks {
		public static void main(String[] args) {
			Scanner sc = new Scanner(System.in);
			int a = sc.nextInt();
			if(a>=80)
			{
				System.out.println("excellent");
			}
			else if(a>=70 || a<60)
			{
				System.out.println("average");
			}
			else
			{
				System.out.println("fail");
			}
			

	}
	}

